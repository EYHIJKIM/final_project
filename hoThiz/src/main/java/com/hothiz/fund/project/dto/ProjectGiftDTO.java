public class ProjectGiftDTO {

	private int project_id;
	private int project_gift_price;
	private String project_gift;
	
	public int getProject_id() {
		return project_id;
	}
	public void setProject_id(int project_id) {
		this.project_id = project_id;
	}
	public int getProject_gift_price() {
		return project_gift_price;
	}
	public void setProject_gift_price(int project_gift_price) {
		this.project_gift_price = project_gift_price;
	}
	public String getProject_gift() {
		return project_gift;
	}
	public void setProject_gift(String project_gift) {
		this.project_gift = project_gift;
	}

	
	
}
